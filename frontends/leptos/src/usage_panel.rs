/// Non-modal sticky panel rendered above the composer row.
/// Visibility is toggled by the "Usage" button in [`crate::composer::Composer`].
///
/// Marked `#[mutants::skip]` — the reactive/DOM body is exercised by the
/// e2e harness; the three pure derivation functions carry the mutation-test
/// budget.
#[mutants::skip]
#[component]
pub fn UsagePanel() -> impl IntoView {
    let store = use_context::<SessionStore>().expect("SessionStore must be provided");
    let panel_open =
        use_context::<UsagePanelOpen>().expect("UsagePanelOpen must be provided");

    // Legend overlay — local to the panel; no context needed.
    let legend_open = RwSignal::new(false);

    // Derive turn counts reactively.
    let turn_row = Memo::new(move |_| {
        let streaming = store.streaming.get();
        store.events.with(|evs| {
            if streaming {
                Some(live_turn_counts(evs))
            } else {
                last_turn_counts(evs)
            }
        })
    });

    // Derive session counts reactively (incl. live turn when streaming).
    let sess_row = Memo::new(move |_| {
        let streaming = store.streaming.get();
        store.events.with(|evs| {
            let base = session_counts(evs);
            if streaming {
                base.add(live_turn_counts(evs))
            } else {
                base
            }
        })
    });

    view! {
        <Show when=move || panel_open.is_open() fallback=|| ()>
            <div class="usage-panel" data-testid="usage-panel">
                <table class="usage-table">
                    <thead>
                        <tr>
                            <th class="ut-row-label ut-header"></th>
                            <th class="ut-header">"in (uncached)"</th>
                            <th class="ut-header">"in (cache write)"</th>
                            <th class="ut-header">"in (cache read)"</th>
                            <th class="ut-header ut-gap">"out"</th>
                            <th class="ut-legend-cell ut-header">
                                <button
                                    class="ut-legend-btn"
                                    data-testid="usage-legend-btn"
                                    title="Token field legend"
                                    on:click=move |_| legend_open.update(|v| *v = !*v)
                                >"ⓘ"</button>
                            </th>
                        </tr>
                    </thead>
                    <tbody>
                        // Turn row — absent until the first turn completes or starts.
                        {move || turn_row.get().map(|c| view! {
                            <tr data-testid="usage-turn-row">
                                <td class="ut-row-label">"turn"</td>
                                <td class="ut-val">{c.fresh_in}</td>
                                <td class="ut-val">{c.cache_write_in}</td>
                                <td class="ut-val">{c.cache_read_in}</td>
                                <td class="ut-val ut-gap">{c.out}</td>
                                <td />
                            </tr>
                        })}
                        // Session row — always present once panel is open.
                        {move || {
                            let c = sess_row.get();
                            view! {
                                <tr data-testid="usage-session-row">
                                    <td class="ut-row-label">"session"</td>
                                    <td class="ut-val">{c.fresh_in}</td>
                                    <td class="ut-val">{c.cache_write_in}</td>
                                    <td class="ut-val">{c.cache_read_in}</td>
                                    <td class="ut-val ut-gap">{c.out}</td>
                                    <td />
                                </tr>
                            }
                        }}
                    </tbody>
                </table>
                <TokenLegend legend_open=legend_open />
            </div>
        </Show>
    }
}

