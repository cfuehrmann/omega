#[cfg(test)]
mod tests {
    #![allow(clippy::unwrap_used, clippy::panic)]

    use super::*;
    use omega_protocol::events::{
        LlmResponseEvent, LlmResponseUsage, TurnEndEvent, TurnMetrics, UserMessageEvent,
    };

    fn user_msg(content: &str) -> OmegaEvent {
        OmegaEvent::UserMessage(UserMessageEvent {
            time: "t".into(),
            content: content.into(),
        })
    }

    fn turn_end(fresh: i64, cw: i64, cr: i64, out: i64) -> OmegaEvent {
        OmegaEvent::TurnEnd(TurnEndEvent {
            time: "t".into(),
            metrics: TurnMetrics {
                input_tokens: fresh,
                output_tokens: out,
                cache_creation_tokens: if cw == 0 { None } else { Some(cw) },
                cache_read_tokens: if cr == 0 { None } else { Some(cr) },
            },
        })
    }

    fn llm_response(fresh: i64, cw: i64, cr: i64, out: i64) -> OmegaEvent {
        OmegaEvent::LlmResponse(LlmResponseEvent {
            time: "t".into(),
            stop_reason: "end_turn".into(),
            cleared_tool_uses: None,
            cleared_input_tokens: None,
            usage: LlmResponseUsage {
                input_tokens: fresh,
                output_tokens: out,
                cache_creation_input_tokens: if cw == 0 { None } else { Some(cw) },
                cache_read_input_tokens: if cr == 0 { None } else { Some(cr) },
                service_tier: None,
            },
            context_hash: "abc".into(),
            text: None,
            thinking: None,
            streaming_start: None,
            response_summary: None,
        })
    }

    // ── last_turn_counts ──────────────────────────────────────────────────

    #[test]
    fn last_turn_counts_none_when_no_events() {
        assert!(last_turn_counts(&[]).is_none());
    }

    #[test]
    fn last_turn_counts_none_when_only_user_message() {
        let evs = vec![user_msg("hi")];
        assert!(last_turn_counts(&evs).is_none());
    }

    #[test]
    fn last_turn_counts_returns_last_turn_end_metrics() {
        let evs = vec![
            turn_end(10, 0, 0, 5),
            user_msg("hi"),
            turn_end(20, 3, 1, 8),
        ];
        let c = last_turn_counts(&evs).unwrap();
        assert_eq!(c.fresh_in, 20);
        assert_eq!(c.cache_write_in, 3);
        assert_eq!(c.cache_read_in, 1);
        assert_eq!(c.out, 8);
    }

    #[test]
    fn last_turn_counts_none_optional_cache_fields_default_to_zero() {
        let evs = vec![turn_end(7, 0, 0, 3)];
        let c = last_turn_counts(&evs).unwrap();
        assert_eq!(c.cache_write_in, 0);
        assert_eq!(c.cache_read_in, 0);
    }

    // ── live_turn_counts ──────────────────────────────────────────────────

    #[test]
    fn live_turn_counts_zero_when_empty() {
        assert_eq!(live_turn_counts(&[]), TokenCounts::default());
    }

    #[test]
    fn live_turn_counts_zero_when_no_llm_response_since_user_msg() {
        let evs = vec![user_msg("hi")];
        assert_eq!(live_turn_counts(&evs), TokenCounts::default());
    }

    #[test]
    fn live_turn_counts_accumulates_responses_since_last_user_msg() {
        let evs = vec![
            user_msg("first"),
            llm_response(100, 0, 0, 50), // should be ignored
            turn_end(100, 0, 0, 50),
            user_msg("second"),
            llm_response(30, 5, 2, 10),
            llm_response(40, 0, 8, 15),
        ];
        let c = live_turn_counts(&evs);
        assert_eq!(c.fresh_in, 70);
        assert_eq!(c.cache_write_in, 5);
        assert_eq!(c.cache_read_in, 10);
        assert_eq!(c.out, 25);
    }

    #[test]
    fn live_turn_counts_includes_events_before_first_user_msg_when_no_user_msg() {
        // If there's no UserMessage at all, all events are included.
        let evs = vec![llm_response(10, 0, 0, 5)];
        let c = live_turn_counts(&evs);
        assert_eq!(c.fresh_in, 10);
        assert_eq!(c.out, 5);
    }

    #[test]
    fn live_turn_counts_ignores_non_llm_response_events() {
        let evs = vec![
            user_msg("hi"),
            turn_end(99, 0, 0, 99), // not an LlmResponse — ignored
            llm_response(20, 0, 0, 8),
        ];
        let c = live_turn_counts(&evs);
        assert_eq!(c.fresh_in, 20);
        assert_eq!(c.out, 8);
    }

    // ── session_counts ────────────────────────────────────────────────────

    #[test]
    fn session_counts_zero_when_empty() {
        assert_eq!(session_counts(&[]), TokenCounts::default());
    }

    #[test]
    fn session_counts_sums_all_turn_ends() {
        let evs = vec![
            user_msg("a"),
            llm_response(5, 0, 0, 2),
            turn_end(10, 2, 1, 4),
            user_msg("b"),
            llm_response(8, 0, 0, 3),
            turn_end(15, 0, 3, 6),
        ];
        let c = session_counts(&evs);
        assert_eq!(c.fresh_in, 25);
        assert_eq!(c.cache_write_in, 2);
        assert_eq!(c.cache_read_in, 4);
        assert_eq!(c.out, 10);
    }

    #[test]
    fn session_counts_ignores_non_turn_end_events() {
        let evs = vec![
            user_msg("hi"),
            llm_response(100, 50, 20, 40),
        ];
        assert_eq!(session_counts(&evs), TokenCounts::default());
    }

    // ── TokenCounts::add ──────────────────────────────────────────────────

    #[test]
    fn token_counts_add_sums_all_fields() {
        let a = TokenCounts { fresh_in: 1, cache_write_in: 2, cache_read_in: 3, out: 4 };
        let b = TokenCounts { fresh_in: 10, cache_write_in: 20, cache_read_in: 30, out: 40 };
        let sum = a.add(b);
        assert_eq!(sum, TokenCounts { fresh_in: 11, cache_write_in: 22, cache_read_in: 33, out: 44 });
    }

    #[test]
    fn token_counts_add_with_zero_is_identity() {
        let a = TokenCounts { fresh_in: 5, cache_write_in: 3, cache_read_in: 1, out: 7 };
        assert_eq!(a.add(TokenCounts::default()), a);
        assert_eq!(TokenCounts::default().add(a), a);
    }
}
