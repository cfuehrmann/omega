/// Fixed-position overlay explaining each token column.
/// Rendered inside `UsagePanel`; toggled by the ⓘ button.
#[component]
fn TokenLegend(legend_open: RwSignal<bool>) -> impl IntoView {
    view! {
        <Show when=move || legend_open.get() fallback=|| ()>
            <div
                class="token-legend-overlay"
                data-testid="token-legend-overlay"
                on:click=move |_| legend_open.set(false)
            >
                <div
                    class="token-legend"
                    on:click=move |e: ev::MouseEvent| e.stop_propagation()
                >
                    <div class="token-legend-header">
                        <span>"Token fields"</span>
                        <button
                            class="token-legend-close"
                            data-testid="token-legend-close"
                            on:click=move |_| legend_open.set(false)
                        >"✕"</button>
                    </div>
                    <table class="token-legend-table">
                        <tbody>
                            <tr>
                                <td><code>"in (uncached)"</code></td>
                                <td>"Tokens processed fresh (standard rate)"</td>
                            </tr>
                            <tr>
                                <td><code>"in (cache write)"</code></td>
                                <td>"Tokens written into prompt cache (1.25×)"</td>
                            </tr>
                            <tr>
                                <td><code>"in (cache read)"</code></td>
                                <td>"Tokens served from prompt cache (0.1×)"</td>
                            </tr>
                            <tr>
                                <td><code>"out"</code></td>
                                <td>"Tokens generated by the model"</td>
                            </tr>
                        </tbody>
                    </table>
                </div>
            </div>
        </Show>
    }
}

