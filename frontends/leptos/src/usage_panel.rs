//! Usage panel — collapsible, sticky token-usage display above the composer.
//!
//! ## Structure
//!
//! ```text
//! App
//!  ├── provide_context::<UsagePanelOpen>
//!  └── UsagePanel           ← renders above Composer, visibility gated by
//!       ├── usage-table      UsagePanelOpen
//!       │    ├── header row: [turn | in (uncached) | in (cache write) |
//!       │    │                       in (cache read) | out | ⓘ]
//!       │    ├── turn row    (last completed turn, or live accumulation)
//!       │    └── session row (session-total, incl. live if streaming)
//!       └── TokenLegend      ← fixed-position overlay toggled by ⓘ
//! ```
//!
//! ## Pure helpers (mutation-tested)
//!
//! - [`last_turn_counts`]  — finds the last `TurnEnd` event's metrics
//! - [`live_turn_counts`]  — sums `LlmResponse` usages since last `UserMessage`
//! - [`session_counts`]    — sums all `TurnEnd` metrics across the session
//!
//! All three operate on the flat `Vec<OmegaEvent>` already held by the
//! `SessionStore`, so no new store signals are needed.

use leptos::ev;
use leptos::prelude::*;
use omega_protocol::OmegaEvent;

use crate::store::SessionStore;

// ---------------------------------------------------------------------------
// Context: panel open/close toggle
// ---------------------------------------------------------------------------

/// Wraps the open/close boolean for the usage panel. Provided as context
/// from `App`; consumed by `Composer` (toggle button) and `UsagePanel`
/// (conditional rendering).
#[derive(Debug, Clone, Copy)]
pub struct UsagePanelOpen(pub RwSignal<bool>);

impl UsagePanelOpen {
    /// Construct closed state. Must run inside a leptos reactive `Owner`.
    #[must_use]
    pub fn new() -> Self {
        Self(RwSignal::new(false))
    }

    /// Toggle open ↔ closed.
    #[mutants::skip] // reactive signal write; covered by e2e harness.
    pub fn toggle(self) {
        self.0.update(|v| *v = !*v);
    }

    /// Whether the panel is currently open.
    pub fn is_open(self) -> bool {
        self.0.get()
    }
}

impl Default for UsagePanelOpen {
    fn default() -> Self {
        Self::new()
    }
}

// ---------------------------------------------------------------------------
// Token count accumulator — pure data
// ---------------------------------------------------------------------------

/// Token breakdown for one logical unit (a turn or the whole session).
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub struct TokenCounts {
    /// Input tokens processed fresh (not from prompt cache).
    pub fresh_in: i64,
    /// Input tokens written into the prompt cache (cache-creation cost).
    pub cache_write_in: i64,
    /// Input tokens served from the prompt cache (cache-read cost).
    pub cache_read_in: i64,
    /// Output tokens generated by the model.
    pub out: i64,
}

impl TokenCounts {
    /// Add `other` to `self` field-by-field.
    #[must_use]
    pub fn add(self, other: Self) -> Self {
        Self {
            fresh_in: self.fresh_in + other.fresh_in,
            cache_write_in: self.cache_write_in + other.cache_write_in,
            cache_read_in: self.cache_read_in + other.cache_read_in,
            out: self.out + other.out,
        }
    }
}

/// Returns the most-recent completed turn's token counts by finding the
/// last `TurnEnd` event in the slice. Returns `None` when no `TurnEnd`
/// has been seen yet.
pub fn last_turn_counts(events: &[OmegaEvent]) -> Option<TokenCounts> {
    events.iter().rev().find_map(|ev| {
        if let OmegaEvent::TurnEnd(e) = ev {
            Some(TokenCounts {
                fresh_in: e.metrics.input_tokens,
                cache_write_in: e.metrics.cache_creation_tokens.unwrap_or(0),
                cache_read_in: e.metrics.cache_read_tokens.unwrap_or(0),
                out: e.metrics.output_tokens,
            })
        } else {
            None
        }
    })
}

/// Accumulates `LlmResponse` token usage since the most recent
/// `UserMessage`. Returns zero counts when no `LlmResponse` has been
/// seen in the current turn yet (e.g. streaming hasn't reached the first
/// response boundary).
pub fn live_turn_counts(events: &[OmegaEvent]) -> TokenCounts {
    let start = events
        .iter()
        .rposition(|ev| matches!(ev, OmegaEvent::UserMessage(_)))
        .map(|i| i + 1)
        .unwrap_or(0);

    let mut acc = TokenCounts::default();
    for ev in &events[start..] {
        if let OmegaEvent::LlmResponse(e) = ev {
            acc.fresh_in += e.usage.input_tokens;
            acc.cache_write_in += e.usage.cache_creation_input_tokens.unwrap_or(0);
            acc.cache_read_in += e.usage.cache_read_input_tokens.unwrap_or(0);
            acc.out += e.usage.output_tokens;
        }
    }
    acc
}

/// Sums token counts from every `TurnEnd` event in the slice. This gives
/// the session-lifetime total for completed turns (live turn added by the
/// caller when streaming).
pub fn session_counts(events: &[OmegaEvent]) -> TokenCounts {
    let mut acc = TokenCounts::default();
    for ev in events {
        if let OmegaEvent::TurnEnd(e) = ev {
            acc.fresh_in += e.metrics.input_tokens;
            acc.cache_write_in += e.metrics.cache_creation_tokens.unwrap_or(0);
            acc.cache_read_in += e.metrics.cache_read_tokens.unwrap_or(0);
            acc.out += e.metrics.output_tokens;
        }
    }
    acc
}

